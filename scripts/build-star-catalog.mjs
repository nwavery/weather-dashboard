// Generates src/data/starCatalog.js from d3-celestial's packaged data
// (Yale Bright Star Catalog + IAU constellation lines, BSD-3-Clause,
// https://github.com/ofrohn/d3-celestial).
//
// Usage: node scripts/build-star-catalog.mjs [stars.6.json] [constellations.lines.json] [starnames.json]
// Falls back to fetching from the d3-celestial repo when paths are omitted.

import { readFileSync, writeFileSync } from 'node:fs';

const BASE = 'https://raw.githubusercontent.com/ofrohn/d3-celestial/master/data';
const MAG_LIMIT = 5.0;     // stars brighter than this make the cut (~1600)
const NAME_MAG_LIMIT = 1.7; // label only the brightest stars (+ Polaris)

async function load(pathArg, url) {
  if (pathArg) return JSON.parse(readFileSync(pathArg, 'utf8'));
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${url}: HTTP ${res.status}`);
  return res.json();
}

const [starsPath, linesPath, namesPath] = process.argv.slice(2);
const stars = await load(starsPath, `${BASE}/stars.6.json`);
const lines = await load(linesPath, `${BASE}/constellations.lines.json`);
const names = await load(namesPath, `${BASE}/starnames.json`);

const ra = (lon) => Math.round(((lon + 360) % 360) * 10) / 10;
const dec1 = (d) => Math.round(d * 10) / 10;

const STARS = [];
const NAMED = [];
for (const f of stars.features) {
  const mag = f.properties?.mag;
  if (typeof mag !== 'number' || mag > MAG_LIMIT) continue;
  const [lon, dec] = f.geometry.coordinates;
  const bv = parseFloat(f.properties.bv);
  STARS.push([ra(lon), dec1(dec), Math.round(mag * 10), Number.isFinite(bv) ? Math.round(bv * 10) : 5]);
  const name = names[String(f.id)]?.name;
  if (name && (mag <= NAME_MAG_LIMIT || name === 'Polaris')) {
    NAMED.push([ra(lon), dec1(dec), name]);
  }
}
STARS.sort((a, b) => a[2] - b[2]);

const CON_LINES = [];
for (const f of lines.features) {
  for (const line of f.geometry.coordinates) {
    CON_LINES.push(line.map(([lon, dec]) => [ra(lon), dec1(dec)]));
  }
}

const header = `// Generated by scripts/build-star-catalog.mjs — do not edit by hand.
// Stars (mag <= ${MAG_LIMIT}) from the Yale Bright Star Catalog and IAU
// constellation lines, via d3-celestial (BSD-3-Clause, Olaf Frohn).
// STARS: [raDeg, decDeg, mag*10, B-V*10] · NAMED: [raDeg, decDeg, name]
// CON_LINES: polylines of [raDeg, decDeg]
`;
const out =
  header +
  `export const STARS = ${JSON.stringify(STARS)};\n` +
  `export const NAMED = ${JSON.stringify(NAMED)};\n` +
  `export const CON_LINES = ${JSON.stringify(CON_LINES)};\n`;

writeFileSync(new URL('../src/data/starCatalog.js', import.meta.url), out);
console.log(`starCatalog.js: ${STARS.length} stars, ${NAMED.length} named, ${CON_LINES.length} polylines, ${(out.length / 1024).toFixed(1)} KB`);
